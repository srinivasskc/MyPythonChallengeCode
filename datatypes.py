# Challenge 3 - Data Types
'''
Write python script to ask for two numbers.
Numbers can be integer or float.
The program should calculate sum of these two numbers.
Print message - "The sum of two numbers is: [sum value]"
'''

num1 = int(input('Enter first number: '))

num2 = int(input('Enter second number: '))

sum = num1 + num2
print("The sum of two numbers is: ", sum)

sub = num1 - num2
print("The subtraction of two numbers is: ", sub)

mul = num1 * num2
print("The multiplication of two numbers is: ", mul)

div = num1 / num2
print("The division of two numbers is: ", div)
